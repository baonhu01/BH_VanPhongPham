    private void btLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLuuActionPerformed
        // TODO add your handling code here:
        if(txtMaNCC.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Không được để mã số rỗng");
        txtMaNCC.requestFocus();
        }
        else{
            String maNCC = txtMaNCC.getText();
            String tenNCC = txtTenNCC.getText();
            String diaChi = txtDiaChi.getText();
            String SDT = txtSDT.getText();
            NhaCungCap ncc = new NhaCungCap();
            ncc.setMaNCC(maNCC);
            ncc.setTenNCC(tenNCC);
            ncc.setDiaChi(diaChi);
            ncc.setsDT(SDT);
            NhaCungCapDAO.themNhaCungCap(ncc);
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            hienThi();
        }
    }//GEN-LAST:event_btLuuActionPerformed

    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        if(tbNhaCungCap.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, "Chọn dòng cần xóa");
            return;
        }
        int xoa = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không", "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (xoa == JOptionPane.YES_OPTION){
            try {
                String mancc = txtMaNCC.getText();
                NhaCungCap ncc = new NhaCungCap();
                ncc.setMaNCC(mancc);
                NhaCungCapDAO.xoaNhaCungCap(mancc);
                JOptionPane.showMessageDialog(null, "Xóa thành công");
                hienThi();
                txtDiaChi.setText("");
                txtMaNCC.setText("");
                txtSDT.setText("");
                txtTenNCC.setText("");
            } catch (Exception e) {
                System.out.println("Không xóa được");
            }
        }  
    }//GEN-LAST:event_btXoaActionPerformed

