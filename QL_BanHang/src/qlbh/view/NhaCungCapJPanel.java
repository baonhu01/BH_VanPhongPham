    public void hienThi() {
        ArrayList<NhaCungCap> dsNCC = NhaCungCapDAO.layDanhSachNhaCungCap();
        dtm.setRowCount(0);
        for(NhaCungCap ncc:dsNCC){
            Vector<Object> vec = new Vector<Object>();
            vec.add(ncc.getMaNCC());
            vec.add(ncc.getTenNCC());
            vec.add(ncc.getDiaChi());
            vec.add(ncc.getsDT());
            dtm.addRow(vec);
    }
        tbNhaCungCap.setModel(dtm);
    }
    
    public void hienThiTK(){
        String maNCC = txtSearch.getText();
        NhaCungCapDAO.timKiemNhaCungCapTheoMa(maNCC);
        ArrayList<NhaCungCap> dsNCC = NhaCungCapDAO.timKiemNhaCungCapTheoMa(maNCC);
        dtm.setRowCount(0);
        for(NhaCungCap ncc:dsNCC){
            Vector<Object> vec = new Vector<Object>();
            vec.add(ncc.getMaNCC());
            vec.add(ncc.getTenNCC());
            vec.add(ncc.getDiaChi());
            vec.add(ncc.getsDT());
            dtm.addRow(vec);
    }
        tbNhaCungCap.setModel(dtm);
    }

